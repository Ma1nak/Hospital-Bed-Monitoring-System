         <strong><font color="#CC0000" size="+2" style="text-decoration:underline" ><h1 align="center"><i>OPERATION THEATRES</i></h1></font></strong><br/><br/>

<p>Operation Theatres are equipped with the state-of-the-art facilities. There are 10 theatres with the most modern and sophisticated equipment. We have seven dedicated OTs for the departments of Neurosurgery, Gynaecology, Emergency, Cardiothoracic surgery, Orthopedics, Ophthalmology and ENT.

<br/><br/>Other theatres are utilized for cases in General Surgery, Plastic Surgery.

<br/><br/>The OTs are maintained in an absolutely sterile and aseptic condition with the help of laminar flow and periodic fumigation, & are supported by CSSD ,one of the best of its kind, and a team of professional and well trained technical staff and nurses.<p><br /><br />

<p class="main">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<font size="+1" style="text-decoration:underline"><strong> MODULAR OT OF HARTFORD HOSPITAL</strong></font><br />

<br /><br />There has been spectacular progress in the development of diagnostic facilities, improved aseptic procedures sophisticated equipment and skills in the recent past. These coupled with safer anesthetic techniques have made surgical intervention feasible for a number of medical entities. It is imperative that operation theatre (OT) is designed scientifically to ensure sterility, easy maintenance and effective utilization.

<br /><br />OT is that specialized facility of the hospital where life saving or life improving procedures are carried out on the human body by invasive methods under strict aseptic conditions in a controlled environment by specially trained personnel to promote healing and cure with maximum safety, comfort and economy.

<br /><br /><strong><font  style="text-decoration:underline">Our Modular OT</strong></font>

<br /><br />• Promotes high standard of asepsis.
<br /><br />• Ensures maximum standard of safety.
<br /><br />• Optimises utilization of OT and staff time.
<br /><br />• Optimises working conditions.
<br /><br />• Patient & staff comfort in terms of thermal, acoustic and lighting requirements.
<br /><br />• Allows flexibility.
<br /><br />• Facilitate coordinated services.
<br /><br />• Minimises maintenance.
<br /><br />• Ensures functional separation of spaces
<br /><br />• Regulates flow of traffic.
<br /><br />• Has all its Cavity and joints filled with epoxy to make an OT seamless.
<br /><br />• Is painted with anti - bacterial & fungal paint.
<br /><br />• Provides soothing environment.

<br /><br />For an unbreachable sterile OT compound, it has been segregated in various zones which are area of varying degrees of cleanliness in which the bacteriological count progressively diminishes from the outer to the inner zones (operating area) and is maintained by a differential decreasing positive pressure ventilation gradient from the inner zone to the outer zone

<br /><br />They are of following types.

<br /><br /><strong><font  style="text-decoration:underline">• Protective Zone: Areas included in this zone are:</strong></font>

<br /><br />o Reception
<br /><br />o Waiting area
<br /><br />o Trolley bay
<br /><br />o Changing room

<br /><br /><font  style="text-decoration:underline"><strong>• Clean Zone: Areas included in this zone are:</strong></font>

<br /><br />o Pre-op room
<br /><br />o Recovery room
<br /><br />o Plaster room
<br /><br />o Staff room
<br /><br />o Store

<br /><br /><font  style="text-decoration:underline"><strong>Sterile Zone: Areas included in this zone are:</strong></font>

<br /><br />o Operating Suite
<br /><br />o Scrub Room
<br /><br />o Anesthesia Induction Room
<br /><br />o Set up Room

<br /><br /><font  style="text-decoration:underline"><strong>Disposal Zone: Areas included in this zone are:</strong></font>

<br /><br />o Dirty Utility
<br /><br />o Disposal corridor

<br /><br /><font  style="text-decoration:underline"><strong>Water Supply:</strong></font>
<br /><br />Besides normal supply of available water at the rate of 400 liters per bed per day separate reserve emergency over head tank is provided for operation theatre.

<br /><br />Safety has always been given the uttermost priority in our hospital and is never compromised come what may!

<br /><br /><font  style="text-decoration:underline"><strong>Fire Safety:</strong></font>
<br /><br />Our OT is provided by both ionization and optical fire detectors as against heat detectors, since equipment - oriented operation theatres are likely to create more smoke than heat in the eventuality of fire. Hydrants and fire extinguishers are at every level possible. Fire exit route is clearly identified, earmarked and well illuminated.<br /><br /><br /><br /><br /><br />



