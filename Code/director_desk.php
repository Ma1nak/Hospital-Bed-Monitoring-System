         <p class="main"><font size="+1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; We have seen phenomenal changes in the practice of medicine over the last few decades. There have been many spectacular changes in both diagnostic and therapeutic fields. Sophisticated biochemical assays, virological markers, precision immunohistochemistry in diagnosing and prognostifying cancer, interventional radiology with access to virtually any part of the body and many more such advances have transformed the practice of medicine forever. Cardiologists are able to treat complex cardiac conditions in the cath lab without patients having to undergo major open heart operations, surgeons are undertaking minimally invasive surgery with much less inconvenience and pain to patients. These changes were unthinkable even 20 years ago. But they have come at a cost â€“ the cost which has been too much in developing nations. The success of modern science has presented to us a situation where the rich can afford to take advantage of scientific miracles while the less privileged are unable to get the benefits which they know are available, but not affordable for them.
<br />

The irony of these scientific advances will haunt the developing world because our health system is so poorly developed. Government hospitals, which should look after those who cannot afford private health care, do not always have the necessary infrastructure or resources in terms of equipment and trained workforce. Private hospitals are mushrooming and many of them are run with a purely corporate ethics. Profitability has taken precedence over compassion for the ailing. Corporate social responsibility has become just another piece of jargon.<br />

The situation should and can change. Private hospitals are here to stay and serve at least those who can afford private health care. The Government must take advantage of the well-trained workforce engaged in these private hospitals and make a partnership so that training of junior doctors is undertaken and recognised in both private and state hospitals. Medicine is an applied science and for us to make good doctors for tomorrow we need to take practical steps today. The practice of encouraging our juniors to sit at home, do some shift work in private nursing homes and study for entrance examinations for postgraduate courses should be changed to a more practical patient-centred programme of training in centres of excellence within both the public and private sectors. 
<br />

Hartford Hospital has excelled in setting very high standards of clinical care. Our results of open heart surgery are comparable to the best international standards; our orthopaedic service has been the trendsetter in this country and has trained more than 50 fellows in joint replacement surgery; our gastroenterology and hepatology department is of an international standard. This hospital has linked up with the Royal College of Physicians of London for the Medical Training Initiative (MTI) scheme where young doctors can go to the UK for 2 years of training. We are constantly working on improving our performance, so that we can deliver the best care for our patients, as well as opening up opportunities for young doctors and nurses.
<br /> 
We believe in private public partnership in selected areas and together, we should be able to make the scientific advances available to everyone so that no one in our country has to die with the knowledge that the death could have been avoided if only he or she could afford it.

