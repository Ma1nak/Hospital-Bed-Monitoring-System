The 19 bed state-of-the-art Emergency Department is one such upgradation which is one of its kind in Eastern India with major operation theatre within the department, 24 hours emergency consultant cover and new fleet of ambulances with life saving equipments.<br /> 

The new Thalassaemia clinic equipped with HPLC also offers organ specific iron overload detection facility by a 1.5T MRI. The upgraded Blood Bank apart from storing blood also makes blood components like fresh /frozen plasma, packed cells and platelets.<br /> 
To ensure infection free treatment to patients the CSSD has also been raised to international standards with specialized equipments. Modular Operation theatres have just started functioning. <br />

The hospital already operates across 40 medical and surgical specialities and is a major referral hub from various neighboring States and countries. It is a conglomerate of experts enriched in their functionalities.<br /> 

The Cardiology and cardio-thoracic department offers state-of-the-art cath lab facilities and a panel of renowned specialists to carry out procedures like cardiac pacing, coronary and peripheral angiographies and angioplasties, embolotherapy etc. Open heart surgeries for coronary artery disease, congenital and acquired valvular heart disease, vascular and thoracic surgeries are routinely done and the results can be compared with the best in the world.<br />

This hospital started doing replacement surgeries for joints before any other hospitals in Eastern India and its faculties take the pride of training many of the established orthopaedic surgeons engaged in doing similar procedures. Many complex operations like reconstructive surgeries of long bones, revision surgeries for replacement of joints etc are carried out routinely. The hospital also provides exceptional trauma care, plastic surgery and spinal surgery. <br />


The centre is running courses on Master of Emergency Management in association with George Washington University, PMaster of Hospital Administration, DNB, and BPT. The hospital is also a proud overseas examination centre for MRCP (Ireland) and Indo-Australian Fellowship in Critical Care Medicine. Peerless Hospital is glad to be the second hospital in India to offer Medical Training Initiative (MTI) Scheme in partnership with Royal College of Physicians (RCP) London. This scheme offers the opportunity of 2 years clinical training and experience in the UK National Health Service (NHS) hospital trusts for suitably qualified international medical graduates. By this Peerless Hospital is bringing home clinical expertise and experience of international standards.<br /> 
Hartford Hospital actively encourages research works and the results are published and presented in prestigious scientific journals and conferences. <br />

The aim of this hospital has been and will remain to be the most ethical, transparent and morally responsible institution. It is committed to combine care with compassion and competence.<br /></font></p>
